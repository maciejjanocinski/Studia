package com.wsei.demo.testowanie_aplikacji;

public class Kalkulator {

  static float dodaj (float a, float b) {
    return a + b;
  }

  static float odejmij (float a, float b) {
    return a - b;
  }

  static float pomnoz(float a, float b) {
    return a * b;
  }

  static float podziel(float a, float b) {
    return a / b;
  }

  static float doPotegi2(float a) {
    return a*a;
  }

  static float pierwiastek(float a) {
    return (float) Math.sqrt(a);
  }
}
